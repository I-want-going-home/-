#include <stdio.h>
#include <stdlib.h>

void star() {
    const char* lines[] = {
        "                           [마그라테아 ver 0.1]                                 ",
        "         풀 한 포기 없는 황무지에서 반짝이는 행성을 만드는 곳 마그라테아,       ",
        "      사람들이 보지 못하는 잠재력을 찾고 전문가의 손길을 더해 보석을 빛나는 곳, ",
        "                      마그라테아에 오신걸 환영합니다.                           ",
        "                                                                                "
    };

    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");

    // 별을 저장할 배열
    const char* stars[] = { "*****", "****", "***", "**", "*", "" };

    // i는 lines 배열 인덱스를 조정
    for (int i = 0; i < 5; i++) {  // lines의 크기가 6이므로 마지막 줄을 제외하고 i < 5로 설정
        // 각 줄의 앞과 뒤에 별을 추가
        printf("%s%s%s\n", stars[4 - i], lines[i], stars[i]);
    }

    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
}

int main() {
    star();
    return 0;
}
